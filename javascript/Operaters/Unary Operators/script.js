// Unary Operators
// These operators operate on a single operand.


// Unary Plus (+)
let a = '5';
console.log(+a); // 5 (converts string to number)

// Unary Negation (-)
let b = 5;
console.log(-a); // 5 (converts to neg number)

// Increment (++)
let c = 5;
console.log(c++); // 5 (Post-increment: returns value first, then increments)
console.log(c);   // 6

let d = 5;
console.log(++d); // 6 (Pre-increment: increments first , then update)
console.log(d);   // 6


// Decrement (--)
let f = 5;
console.log(f--); // 5 (Post-decrement: returns value first, then decrements)
console.log(f);   // 4

let g = 5;
console.log(--g); // 4 (Pre-decrement: update decrements first then print value)
console.log(g);   // 4

