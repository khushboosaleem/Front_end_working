// Let's Explore Arithmetic Operators

// Arithmetic Operators: +, -, *, /, %, **
let a = 5
let b = 5
console.log(a+b)  // 10
console.log(a-b)  // 0
console.log(a*b)  //25
console.log(a/b)  // 1
console.log(a**b)  // 3125
console.log(a%b)   // 0